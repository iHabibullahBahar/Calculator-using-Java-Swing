    public void mathmeticalOperation()
    {
        switch(clac)
        {
            case 1:
                String k=jTextField1.getText();
                answer=number+Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(answer));
                jTextField2.setText(number+ "+"+k+" = "+answer);
                break;
            case 2:
                 k=jTextField1.getText();
                answer=number-Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(answer));
                jTextField2.setText(number+ "-"+k+" = "+answer);
                break;
            case 3:
                k=jTextField1.getText();
                answer=number*Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(answer));
                jTextField2.setText(number+ "x"+k+" = "+answer);
                break;
            case 4:
                k=jTextField1.getText();
                answer=number/Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(answer));
                jTextField2.setText(Double.toString(number)+ "/"+k+" = "+answer);
                break;
            case 5:
                answer=Math.sqrt(Double.parseDouble(jTextField1.getText()));
                jTextField1.setText(Double.toString(answer));
                break;
            case 6:
                answer=number%Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(answer));
                break;
            case 7:
                answer=Math.sqrt(Double.parseDouble(jTextField1.getText()));
                jTextField1.setText(Double.toString(answer));
                break;
        }
            
    }

